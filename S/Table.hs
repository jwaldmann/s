value :: T -> Maybe Int
value t = case t of
    S -> return 0
    App {fun=f,arg=a} -> do
       vf <- value f
       va <- value a
       M.lookup (vf,va) trans

normalizing :: T -> Bool
normalizing t = isJust $ value t

complete :: M.Map (Int,Int) Int -> M.Map (Int,Int) Int
complete t = 
    let s = succ $ maximum $ M.elems t
    in  M.fromList $ do
            p <- [ 0 .. s ] ; q <- [ 0 .. s ] 
            return ((p,q), M.findWithDefault s (p,q) t)

